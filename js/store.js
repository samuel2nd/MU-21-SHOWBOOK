  // Helper: Get RTR ID for a SHOW device (1-indexed source number)
  function getShowRtrId(showNum) {
    if (showNum >= 1 && showNum <= 20) return 864 + showNum;   // 865-884
    if (showNum >= 21 && showNum <= 40) return 1150 + showNum; // 1171-1190
    if (showNum >= 41 && showNum <= 60) return 1161 + showNum; // 1202-1221
    if (showNum >= 61 && showNum <= 80) return 1161 + showNum; // 1222-1241
    return 0;
  }

  // Sync all showNames from sources to rtrMaster deviceNames
  function syncShowNamesToRtrMaster() {
    if (!data.sources || !data.rtrMaster) return;

    for (let i = 0; i < 80; i++) {
      const showName = data.sources[i]?.showName;
      const rtrId = getShowRtrId(i + 1);
      if (!rtrId) continue;

      // Find the rtrMaster entry with this RTR ID
      const idx = data.rtrMaster.findIndex(d => d && d.row === rtrId);
      if (idx !== -1) {
        // Update deviceName to match showName (or default if empty)
        const newName = showName || `SHOW ${String(i + 1).padStart(2, '0')}`;
        data.rtrMaster[idx].deviceName = newName;
      }
    }
  }

  // Migrate old SWR output names to match RTR I/O naming
  function migratSwrNames() {
    if (!data.swrIo || !data.swrIo.outputs) return;

    // Name mappings: old → new
    const nameMap = {
      'SWPVW': 'SWR PVW',
      'AUX 1': 'AUX 01', 'AUX 2': 'AUX 02', 'AUX 3': 'AUX 03',
      'AUX 4': 'AUX 04', 'AUX 5': 'AUX 05', 'AUX 6': 'AUX 06',
      'AUX 7': 'AUX 07', 'AUX 8': 'AUX 08', 'AUX 9': 'AUX 09',
      'IS 1': 'IS 01', 'IS 2': 'IS 02', 'IS 3': 'IS 03',
      'IS 4': 'IS 04', 'IS 5': 'IS 05', 'IS 6': 'IS 06',
      'IS 7': 'IS 07', 'IS 8': 'IS 08', 'IS 9': 'IS 09',
    };

    let migrated = 0;
    data.swrIo.outputs.forEach(output => {
      if (output.show && nameMap[output.show]) {
        output.show = nameMap[output.show];
        migrated++;
      }
      if (output.defaultShow && nameMap[output.defaultShow]) {
        output.defaultShow = nameMap[output.defaultShow];
        migrated++;
      }
    });

    if (migrated > 0) {
      console.log(`Migrated ${migrated} SWR output names to match RTR I/O`);
    }
  }

  // Load from localStorage
  function loadFromStorage() {
    try {
      const saved = localStorage.getItem(STORAGE_KEY);
      if (saved) {
        const parsed = JSON.parse(saved);
        data = mergeDeep(emptyShow(), parsed);
        if (!data.fiberTac || Object.keys(data.fiberTac).length === 0) {
          data.fiberTac = initTacData();
        }
        // Ensure all tacPanels have corresponding fiberTac entries
        const tacPanels = data.sheet8?.tacPanels || [];
        tacPanels.forEach(panelName => {
          if (!data.fiberTac[panelName]) {
            data.fiberTac[panelName] = Array.from({ length: 24 }, (_, i) => ({ port: i + 1, source: '', dest: '', notes: '' }));
          }
        });
        // Ensure sources array always has 80 items
        if (!data.sources || !Array.isArray(data.sources) || data.sources.length < 80) {
          const defaultSources = emptyShow().sources;
          data.sources = data.sources || [];
          for (let i = 0; i < 80; i++) {
            if (!data.sources[i]) {
              data.sources[i] = defaultSources[i];
            } else {
              // Ensure each source has all required fields
              data.sources[i] = { ...defaultSources[i], ...data.sources[i] };
            }
          }
        }
        // Sync showNames from sources to rtrMaster deviceNames
        syncShowNamesToRtrMaster();
        // Migrate txPgmGfx to new structure
        if (data.txPgmGfx) {
          const defaults = emptyShow().txPgmGfx;
          // Migrate TX rows
          if (data.txPgmGfx.tx) {
            for (let i = 0; i < 8; i++) {
              if (data.txPgmGfx.tx[i]) {
                // Migrate old field names if present
                const old = data.txPgmGfx.tx[i];
                data.txPgmGfx.tx[i] = {
                  ...defaults.tx[i],
                  ...old,
                  daInput: old.daInput || old.device || defaults.tx[i].daInput,
                  engSource: old.engSource || old.source || defaults.tx[i].engSource,
                  audioSource: old.audioSource || old.audioPick || defaults.tx[i].audioSource,
                };
                delete data.txPgmGfx.tx[i].audioPick; // Remove old field
                delete data.txPgmGfx.tx[i].ioRtrOut; // Remove, now computed
              } else {
                data.txPgmGfx.tx[i] = defaults.tx[i];
              }
            }
          } else {
            data.txPgmGfx.tx = defaults.tx;
          }
          // Migrate CG rows
          if (data.txPgmGfx.cg) {
            for (let i = 0; i < 6; i++) {
              if (data.txPgmGfx.cg[i]) {
                const old = data.txPgmGfx.cg[i];
                data.txPgmGfx.cg[i] = {
                  ...defaults.cg[i],
                  ...old,
                  daInput: old.daInput || old.device || defaults.cg[i].daInput,
                  engSource: old.engSource || old.source || defaults.cg[i].engSource,
                };
              } else {
                data.txPgmGfx.cg[i] = defaults.cg[i];
              }
            }
          } else {
            data.txPgmGfx.cg = defaults.cg;
          }
          // Ensure canvas section exists
          if (!data.txPgmGfx.canvas) {
            data.txPgmGfx.canvas = defaults.canvas;
          } else {
            for (let i = 0; i < 8; i++) {
              data.txPgmGfx.canvas[i] = { ...defaults.canvas[i], ...(data.txPgmGfx.canvas[i] || {}) };
            }
          }
          // Migrate PGM rows (now 4 rows instead of 1)
          if (!data.txPgmGfx.pgm || data.txPgmGfx.pgm.length < 4) {
            const oldPgm = data.txPgmGfx.pgm || [];
            data.txPgmGfx.pgm = defaults.pgm.map((def, i) => ({
              ...def,
              ...(oldPgm[i] || {}),
              audioSource: (oldPgm[i] && (oldPgm[i].audioSource || oldPgm[i].audioPick)) || def.audioSource,
            }));
          } else {
            for (let i = 0; i < 4; i++) {
              const old = data.txPgmGfx.pgm[i] || {};
              data.txPgmGfx.pgm[i] = {
                ...defaults.pgm[i],
                ...old,
                daInput: old.daInput || old.device || defaults.pgm[i].daInput,
                engSource: old.engSource || old.source || defaults.pgm[i].engSource,
                audioSource: old.audioSource || old.audioPick || defaults.pgm[i].audioSource,
              };
              delete data.txPgmGfx.pgm[i].audioPick; // Remove old field
            }
          }
          // Remove old remi section if it exists (no longer used)
          delete data.txPgmGfx.remi;
        }
        // Migrate RTR Outputs - add any missing devices from defaults
        if (data.rtrOutputs && Array.isArray(data.rtrOutputs)) {
          const defaultOutputs = defaultRtrOutputs();
          const existingIds = new Set(data.rtrOutputs.map(d => d.row));
          let addedCount = 0;
          for (const defDevice of defaultOutputs) {
            if (!existingIds.has(defDevice.row)) {
              data.rtrOutputs.push(defDevice);
              addedCount++;
            }
          }
          if (addedCount > 0) {
            // Sort by row number after adding
            data.rtrOutputs.sort((a, b) => a.row - b.row);
            console.log(`Migrated ${addedCount} new RTR output devices`);
          }
        }
        // Migrate RTR Master - add any missing devices from defaults (audio-only sources, etc.)
        if (data.rtrMaster && Array.isArray(data.rtrMaster)) {
          const defaultMaster = defaultRtrMaster();
          const existingIds = new Set(data.rtrMaster.map(d => d.row));
          let addedCount = 0;
          for (const defDevice of defaultMaster) {
            if (!existingIds.has(defDevice.row)) {
              data.rtrMaster.push(defDevice);
              addedCount++;
            }
          }
          if (addedCount > 0) {
            data.rtrMaster.sort((a, b) => a.row - b.row);
            try {
              localStorage.setItem(STORAGE_KEY, JSON.stringify(data));
            } catch (e) {
              console.warn('Failed to save RTR master migration:', e);
            }
          }
        }
        // Migrate SWR output names to match RTR I/O naming
        migratSwrNames();
      }
    } catch (e) {
      console.warn('Failed to load from localStorage:', e);
    }
  }

  function saveToStorage() {
    try {
      localStorage.setItem(STORAGE_KEY, JSON.stringify(data));
    } catch (e) {
      console.warn('Failed to save to localStorage:', e);
    }
  }

  // Deep merge utility
  function mergeDeep(target, source) {
    if (!source) return target;
    const output = { ...target };
    for (const key of Object.keys(source)) {
      if (source[key] && typeof source[key] === 'object' && !Array.isArray(source[key])) {
        output[key] = mergeDeep(target[key] || {}, source[key]);
      } else {
        output[key] = source[key];
      }
    }
    return output;
  }

  // Event system
  function on(event, callback) {
    if (!listeners[event]) listeners[event] = [];
    listeners[event].push(callback);
  }

  function emit(event, payload) {
    (listeners[event] || []).forEach(cb => cb(payload));
  }

  // Public API
  return {
    get data() { return data; },

    get(path) {
      return path.split('.').reduce((obj, key) => obj?.[key], data);
    },

    set(path, value) {
      const keys = path.split('.');
      let obj = data;
      for (let i = 0; i < keys.length - 1; i++) {
        if (!obj[keys[i]]) obj[keys[i]] = {};
        obj = obj[keys[i]];
      }
      obj[keys[keys.length - 1]] = value;
      // Increment version and timestamp on every change (unless setting version/lastModified directly)
      if (path !== 'show.version' && path !== 'show.lastModified') {
        data.show.version = (data.show.version || 0) + 1;
        data.show.lastModified = Date.now();
      }
      saveToStorage();
      emit('saved', Date.now());
      emit('change', { path, value });
      emit(`change:${keys[0]}`, { path, value });
    },

    loadShow(newData) {
      data = mergeDeep(emptyShow(), newData);
      // Ensure version tracking exists (migrate existing shows)
      if (typeof data.show.version !== 'number') {
        data.show.version = 0;
      }
      if (typeof data.show.lastModified !== 'number') {
        data.show.lastModified = Date.now();
      }
      if (!data.fiberTac || Object.keys(data.fiberTac).length === 0) {
        data.fiberTac = initTacData();
      }
      // Ensure all tacPanels have corresponding fiberTac entries
      const tacPanels = data.sheet8?.tacPanels || [];
      tacPanels.forEach(panelName => {
        if (!data.fiberTac[panelName]) {
          data.fiberTac[panelName] = Array.from({ length: 24 }, (_, i) => ({ port: i + 1, source: '', dest: '', notes: '' }));
        }
      });
      // Migrate RTR Outputs - add any missing devices from defaults
      if (data.rtrOutputs && Array.isArray(data.rtrOutputs)) {
        const defaultOutputs = defaultRtrOutputs();
        const existingIds = new Set(data.rtrOutputs.map(d => d.row));
        let addedCount = 0;
        for (const defDevice of defaultOutputs) {
          if (!existingIds.has(defDevice.row)) {
            data.rtrOutputs.push(defDevice);
            addedCount++;
          }
        }
        if (addedCount > 0) {
          data.rtrOutputs.sort((a, b) => a.row - b.row);
          console.log(`Migrated ${addedCount} new RTR output devices`);
        }
      }
      // Migrate RTR Master - add any missing devices from defaults (audio sources, etc.)
      if (data.rtrMaster && Array.isArray(data.rtrMaster)) {
        const defaultMaster = defaultRtrMaster();
        const existingIds = new Set(data.rtrMaster.map(d => d.row));
        let addedCount = 0;
        for (const defDevice of defaultMaster) {
          if (!existingIds.has(defDevice.row)) {
            data.rtrMaster.push(defDevice);
            addedCount++;
          }
        }
        if (addedCount > 0) {
          data.rtrMaster.sort((a, b) => a.row - b.row);
        }
      }
      // Migrate SWR output names to match RTR I/O naming
      migratSwrNames();
      // Sync showNames from sources to rtrMaster deviceNames
      syncShowNamesToRtrMaster();
      saveToStorage();
      emit('show-loaded', data);
    },

    newShow(name, format) {
      data = emptyShow();
      data.fiberTac = initTacData();
      data.show.name = name || '';
      data.show.format = format || '';
      saveToStorage();
      emit('show-loaded', data);
    },

    exportData() {
      return JSON.parse(JSON.stringify(data));
    },

    save() {
      saveToStorage();
    },

    on,
    emit,

    init() {
      loadFromStorage();
      // Ensure audio-only sources exist in rtrMaster (runs after all loading complete)
      this.ensureAudioSources();
    },

    // Ensure all audio-only sources from defaults exist in rtrMaster
    ensureAudioSources() {
      const defaultMaster = defaultRtrMaster();
      const existingIds = new Set(data.rtrMaster.map(d => d.row));
      let addedCount = 0;

      for (const defDevice of defaultMaster) {
        if (defDevice.row >= 2000 && !existingIds.has(defDevice.row)) {
          data.rtrMaster.push(defDevice);
          addedCount++;
        }
      }

      if (addedCount > 0) {
        data.rtrMaster.sort((a, b) => a.row - b.row);
        saveToStorage();
      }
    },
  };
